#include "lexer.h"

int ft_isspace(int c)
{
    if (c == ' ' || c == '\f' || c == '\n' || c == '\r' || c == '\t' || c == '\v')
        return (1);
    return (0);
}

int ft_ismetacharacter(int c)
{
    if (c == '|' || c == '<' || c == '>' || c == '&' || c == '(' || c == ')')
        return (1);
    return (0);
}